import { collection, addDoc, doc, writeBatch } from "https://www.gstatic.com/firebasejs/10.8.1/firebase-firestore.js";

export async function saveIndividualEntry(db, userId, category, entryData) {
    if (!userId) throw new Error("User not authenticated.");
    if (!entryData.name) throw new Error("Name/Title is required!");
    
    const finalData = { ...entryData, isMerged: false, createdAt: new Date().toISOString() };
    const newDocRef = await addDoc(collection(db, "users", userId, category), finalData);
    return newDocRef.id;
}

export async function saveBulkEntries(db, userId, category, entriesArray) {
    if (!userId || !entriesArray || entriesArray.length === 0) throw new Error("Invalid save request.");
    
    const batch = writeBatch(db);
    
    // Safety check: Filter out any parsing errors generated by the engine
    const validEntries = entriesArray.filter(entry => entry.status !== 'error');
    if (validEntries.length === 0) throw new Error("No valid data to process.");

    validEntries.forEach(entry => {
        const newRef = doc(collection(db, "users", userId, category));
        batch.set(newRef, { isMerged: false, ...entry });
    });
    
    await batch.commit();
    return validEntries.length;
}

/**
 * High-performance movie metadata parsing engine.
 * Strict conversion of bulk movie entries into structured database schema.
 */
export function parseBulkText(text) {
    if (!text) return [];

    const lines = text.split(/\r?\n/);
    const results = [];

    // Process the text block in a single, highly-performant pass
    for (let i = 0; i < lines.length; i++) {
        const originalLine = lines[i];
        const line = originalLine.trim();

        // Rule: Continue processing all remaining entries. Never terminate because of a single invalid line.
        if (!line) continue;

        // Rule: Everything before the first opening parenthesis '(' is the title.
        const firstParenIndex = line.indexOf('(');
        if (firstParenIndex === -1) {
            results.push({
                status: "error",
                input: originalLine,
                reason: "Missing parentheses. Could not reliably locate metadata blocks."
            });
            continue;
        }

        // Rule: Remove only unnecessary leading and trailing whitespace. Preserve internal spaces.
        const title = line.substring(0, firstParenIndex).trim();
        const remainder = line.substring(firstParenIndex);

        // Extract raw string blocks encapsulated by parentheses
        const groupRegex = /\(([^)]*)\)/g;
        const groups = [];
        let match;
        while ((match = groupRegex.exec(remainder)) !== null) {
            // Trim surrounding whitespace from the group content itself
            groups.push(match[1].trim());
        }

        // Integrity Check: Ensure exact schema match to prevent shifted data
        if (groups.length !== 6) {
            results.push({
                status: "error",
                input: originalLine,
                reason: `Expected exactly 6 metadata groupings, but found ${groups.length}.`
            });
            continue;
        }

        const [yearStr, language, category, director, genreStr, castStr] = groups;

        // Rule: Year must be a four-digit integer. Return as a number. If invalid, set null and report the issue.
        let yearNum = null;
        if (/^\d{4}$/.test(yearStr)) {
            yearNum = parseInt(yearStr, 10);
        } else {
            console.warn(`[Parser Engine] Invalid Year format: "${yearStr}" in line: "${originalLine}". Value set to null.`);
        }

        // Rule: Genres and Cast split only on commas, return arrays, preserve order and exact spelling.
        const genres = genreStr ? genreStr.split(',').map(s => s.trim()).filter(Boolean) : [];
        const cast = castStr ? castStr.split(',').map(s => s.trim()).filter(Boolean) : [];

        results.push({
            name: title,
            Year: yearNum,
            Language: language,
            Category: category,
            Director: director,
            Genre: genres,
            Cast: cast
        });
    }

    return results;
}
